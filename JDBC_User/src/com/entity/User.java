package com.entity;

public class User {
	
	private int roono;
	private String name;
	private double marks;
	private String dep;
	public int getRoono() {
		return roono;
	}
	public void setRoono(int roono) {
		this.roono = roono;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getMarks() {
		return marks;
	}
	public void setMarks(double marks) {
		this.marks = marks;
	}
	public String getDep() {
		return dep;
	}
	public void setDep(String dep) {
		this.dep = dep;
	}
		
	

}
